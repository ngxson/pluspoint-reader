uint32_t uzlib_crc32(const void *data, unsigned int length, uint32_t crc) {
  const uint8_t *buf = (const uint8_t *)data;
  crc ^= 0xffffffffu;
  for (unsigned int i = 0; i < length; i++) {
    crc = crc32_table[(crc ^ buf[i]) & 0xff] ^ (crc >> 8);
  }
  return crc ^ 0xffffffffu;
}
